    private void jPanel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel17MouseClicked
 String nombre=txtNombre.getText();
 String precio=txtPrecio.getText();
 String descripcion=txtDescripcion.getText();
 String imagen=txtPrecio1.getText();
        
        
        if (nombre.isEmpty() || precio.isEmpty() || descripcion.isEmpty() || imagen.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Todos los campos deben estar llenos.");
        return;
    }
         int confirm = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que desa modificar el plan?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) return;

         Conexion conexion = new Conexion();
    Connection conn = null;
 try {
        conexion.conectar();
        conn = conexion.getConexion();

        
        String sql1 = "UPDATE Planes SET precio = ?,descripcion=?,imagen=? WHERE nombre = ?";
        PreparedStatement stmt1 = conn.prepareStatement(sql1);
        stmt1.setString(1, precio);
        stmt1.setString(2, descripcion);
         stmt1.setString(3, imagen);
        stmt1.setString(4, nom); 
        
        stmt1.executeUpdate();
        stmt1.close();

       
        String sql2 = "UPDATE Planes SET nombre = ? WHERE precio = ? AND descripcion=?";
        PreparedStatement stmt2 = conn.prepareStatement(sql2);
        stmt2.setString(1, nombre);
        stmt2.setString(2, precio);
        stmt2.setString(3, descripcion);
       
        stmt2.executeUpdate();
        stmt2.close();

        JOptionPane.showMessageDialog(null, "Plan modificado exitosamente.");
        
        
        Recepcionista recepcionista = new Recepcionista();
        recepcionista.setVisible(true);
        this.dispose();

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al modificar usuario:\n" + ex.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
    } finally {
        conexion.cerrar();
    }       
    }//GEN-LAST:event_jPanel17MouseClicked

