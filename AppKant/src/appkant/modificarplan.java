 public void cargarPlan() {
  Conexion conexion = new Conexion();
    Connection conn = null;

    try {
        conexion.conectar();
        conn = conexion.getConexion();

        String sql = "SELECT descripcion, precio , imagen FROM Planes WHERE nombre = ?" ;
                     
                   

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, this.nom);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            txtNombre.setText(nom);
            txtDescripcion.setText(rs.getString("Descripcion"));
            txtPrecio.setText(rs.getString("precio"));
            txtPrecio1.setText(rs.getString("imagen"));
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el usuario con ese ID.");
        }

        rs.close();
        ps.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al cargar datos del usuario:\n" + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
    } finally {
        conexion.cerrar();
    }
       
 }
