 public void cargarUsuario() {
    Conexion conexion = new Conexion();
    Connection conn = null;

    try {
        conexion.conectar();
        conn = conexion.getConexion();

        String sql = "SELECT u.Nombre, u.Password, u.Correo, u.Direccion, u.Telefono, u.FechaNacimiento " +
                     "FROM Usuarios u WHERE u.UsuarioID = ?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, this.usuarioID);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            txtNombre.setText(rs.getString("Nombre"));

            // 游댑 Desencriptar la contrase침a antes de mostrarla
            String passwordEncriptada = rs.getString("Password");
            String passwordDesencriptada = EncriptadorAES.desencriptar(passwordEncriptada);
            txtContrasena.setText(passwordDesencriptada);

            txtCorreo.setText(rs.getString("Correo"));
            txtDireccion.setText(rs.getString("Direccion"));
            txtTelefono.setText(rs.getString("Telefono"));

            String fecha = rs.getString("FechaNacimiento");
            String fechaTexto = fecha;

            SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");

            try {
                java.util.Date fechaUtil = formato.parse(fechaTexto);
                java.sql.Date fechaSql = new java.sql.Date(fechaUtil.getTime());
                jdtCalendario.setDate(fechaSql);
            } catch (ParseException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se encontr칩 el usuario con ese ID.");
        }

        rs.close();
        ps.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al cargar datos del usuario:\n" + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
    } finally {
        conexion.cerrar();
    }
}

