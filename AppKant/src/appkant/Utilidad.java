    public static void insertarEnBaseDeDatos(String id, String usuario, String tipoUsuario, String contrasena) throws SQLException, ClassNotFoundException {
            if (buscarByUser(usuario)) {
                JOptionPane.showMessageDialog(null, "El usuario ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String sql = "INSERT INTO Usuario (id, usuario, type_user, contra) VALUES (?, ?, ?, ?)";
            try (Connection conn = Conexion.obtenerConexion();
                 var stmt = conn.prepareStatement(sql)) {
                stmt.setString(1, id);
                stmt.setString(2, usuario);
                stmt.setString(3, tipoUsuario);
                stmt.setString(4, contrasena);
                int filasAfectadas = stmt.executeUpdate();
                if (filasAfectadas > 0) {
                    JOptionPane.showMessageDialog(null, "Datos insertados correctamente");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al insertar datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    public static boolean buscarByUser(String u) throws ClassNotFoundException {
            String sql = "SELECT usuario FROM Usuario WHERE usuario = ?";
            Connection conn = Conexion.obtenerConexion();
            try (var stmt = conn.prepareStatement(sql)) {
                stmt.setString(1, u);
                var rs = stmt.executeQuery();
                return rs.next();
            } catch (SQLException ex) {
                Logger.getLogger(Utilidad.class.getName()).log(Level.SEVERE, null, ex);
            }
            return false;
        }

