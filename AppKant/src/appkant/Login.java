   private String validarUsuario(String claveUsuario, String password) throws SQLException {
        String sql = "SELECT rol FROM Usuarios WHERE Correo=? AND Password = ? AND Estatus = 'activo'";
      //  String sql2="SELECT Correo FROM Contactos WHERE Correo = ? ";
        try (PreparedStatement stmt = conn.prepareStatement(sql)) {
            stmt.setString(1,claveUsuario);
            stmt.setString(2, password);
            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    return rs.getString("rol");
                }
            }
        }
        return null;
    }


