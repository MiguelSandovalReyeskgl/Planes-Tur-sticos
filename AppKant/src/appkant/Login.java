    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
String usuario = user.getText().trim();
String contrasenaIngresada = new String(pass.getPassword()).trim();

try {
    conexionBD = new Conexion();
    conexionBD.conectar();
    conn = conexionBD.getConexion();

    String sql = "SELECT u.Password, r.nombre_rol FROM Usuarios u " +
                 "INNER JOIN Roles r ON u.rol = r.rolID " +
                 "WHERE u.Correo = ?";

    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setString(1, usuario);

    ResultSet rs = ps.executeQuery();

    if (rs.next()) {
        String passwordEncriptada = rs.getString("Password");
        String passwordDesencriptada = EncriptadorAES.desencriptar(passwordEncriptada);

        if (contrasenaIngresada.equals(passwordDesencriptada)) {
            String rolNombre = rs.getString("nombre_rol");
            String rolNombreNormalizado = rolNombre.trim().toLowerCase();

            if (rolNombreNormalizado.equals("administrador")) {
                Recepcionista p = new Recepcionista(); 
                p.setVisible(true);
                this.dispose();
            } else if (rolNombreNormalizado.equals("recepcionista")) {
                Comprau p1 = new Comprau(usuario); 
                p1.setVisible(true);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Contraseña incorrecta.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Correo no registrado.",
            "Error", JOptionPane.ERROR_MESSAGE);
    }

} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this, "Error al validar usuario: " + ex.getMessage(),
        "Error", JOptionPane.ERROR_MESSAGE);
}
finally {
    conexionBD.cerrar();
    System.out.println("Conexión cerrada");
}


    }//GEN-LAST:event_jLabel7MouseClicked

